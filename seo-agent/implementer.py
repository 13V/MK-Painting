def _build_pr_body(changes):
    """Build PR description markdown."""
    lines = [
        "## SEO Optimizations\n",
        "Changes generated by the MK Painting SEO Agent based on Google Search Console data.\n",
        "### Changes\n",
        "| File | Type | Description |",
        "|---|---|---|",
    ]

    for c in changes:
        lines.append(f"| `{c['file']}` | {c['change_type']} | {c['description']} |")

    lines.extend([
        "",
        "### Review Checklist",
        "- [ ] Title tags are under 60 characters",
        "- [ ] Meta descriptions are 120-155 characters",
        "- [ ] Changes match the target keywords",
        "",
        "---",
        "*Generated by MK Painting SEO Agent*",
    ])

    return "\n".join(lines)
